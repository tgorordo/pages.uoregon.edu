# Ray Optics via Fermat's Principle
"""

# ╔═╡ 68192225-a615-4b64-bc45-5e1886317dd3
md"""
Now that you've seen ray optics as originating from Huygens' construction (i.e. by thinking about the propagation of wavefronts with rays following the radial directions to the fronts [Lec20]) - you might find it interesting to learn that there is a geometrically [*equivalent*](https://en.wikipedia.org/wiki/Fermat%27s_principle#Equivalence_to_Huygens'_construction) perspective that can be stated very simply:
"""

# ╔═╡ e6d760a3-7b65-4e73-b96b-817e736c6086
md"""
**Fermat's Principle**: *The geometric path that a ray of light takes between two points is the path which takes it the least time to travel along.*
"""

# ╔═╡ 35b36abd-6de0-4403-8d04-aec73663cb3f
degree = π / 180;

# ╔═╡ d6f429e4-cbb0-443c-93d9-ad4fe64a42c6
md"""
This is an example of an *action principle* - a statement about physics that is phrased in terms of an *optimization* (minimization/maximization - i.e. extremization)
of a global property instead of in terms of local coordinates and/or rates of change. The physics you've seen so far has mostly presented physical laws as the latter kind of local statements (e.g. the Huygens' construction describes how small parts of wavefronts act as local point sources that determine the propagation of the front nearby) - but it turns out that most of it can also be rephrased in terms of action principles! Such global approaches provide powerful and profound analytic tools that lie at the root of most modern physics.

(an important caveat: certain non-conservative systems such as those with friction or drag cannot be rephrased this way)

The math required to understand that translation in general is beyond a reasonable scope for this course, but the relevant action principle for ray optics **is** simple enough we can see that it works and spookily reproduces now-familiar results - and so get a hint at things to come: 
"""

