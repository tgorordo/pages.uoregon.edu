# ╔═╡ 645483aa-a466-4e88-b5c4-cdb9e3de4e89
md"""
The total travel time of the ray along a particular path can be written in terms of the geometry and the (unknown) point of incidence:

$T = \int dt = \int \frac{dt}{dl} dl = \frac{l_1}{v_1} + \frac{l_2}{v_2} = \frac{\sqrt{x^2 + h^2}}{v_1} + \frac{\sqrt{(w - x)^2 + d^2}}{v_2}$

where the light velocities in each region are related to the indices of refraction and the speed of light: 

$v_1 = c/n_1$

$v_2 = c/n_2$

Fermat's principle says that the actual ray of light that goes from the sun to the fish will pass through the surface at the point which leads to a minimal travel time. To ask for such a point we can extremize $T$, demanding that the point of incidence be such that:

$\frac{dT}{dx} = \frac{n_1 x}{c \sqrt{x^2 + h^2}} - \frac{n_2(w-x)}{c \sqrt{(w-x)^2 + d^2}} = 0$

Notice, now, that in terms of the incident and refracted angles we can write things trigonometrically and get a much simpler form:

$\frac{dT}{dx} = \frac{n_1\sin\theta_1}{c} - \frac{n_2\sin\theta_2}{c} = 0$

which is just Snell's law! Rearranging:

$n_1 \sin\theta_1 = n_2 \sin\theta_2$

That is - the point of incidence on the surface that minimizes the total travel time
is **exactly** the point that geometrically obeys Snell's law!

"""

